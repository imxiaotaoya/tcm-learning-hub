fs.mkdirSync(OUT, { recursive: true });

console.log("── 1. 方剂方证 ──"); buildFormulas();
console.log("\n── 2. 课次 + 章节 ──"); buildLessons();
console.log("\n── 3. 截图索引 ──"); buildScreenshots();
console.log("\n── 4. 六经辨证 ──"); buildSixMeridian();
console.log("\n── 5. 症状路由 ──"); buildSymptomRoutes();

console.log("\n✅ 数据管道完成\n");
